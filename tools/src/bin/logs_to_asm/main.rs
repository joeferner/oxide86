mod config;
mod generate;
mod parse;

use std::fs::File;
use std::io::{BufWriter, Write};
use std::path::PathBuf;

use anyhow::{Context, Result};
use clap::Parser;
use config::{Config, load_config};
use generate::generate;
use parse::parse_log;

#[derive(Parser)]
#[command(
    name = "logs-to-asm",
    about = "Convert oxide86 execution log to annotated assembly listing"
)]
struct Args {
    /// Path to the log file
    #[arg(long, short, default_value = "oxide86.log")]
    log_file: PathBuf,

    /// Path to optional JSON config file
    #[arg(long, short)]
    config: Option<PathBuf>,

    /// Output file path
    #[arg(long, short)]
    out: PathBuf,

    /// Mark instructions executed at least this many times with [HOT]
    #[arg(long, default_value_t = 1000)]
    hot_threshold: u64,
}

fn main() -> Result<()> {
    let args = Args::parse();

    let result = parse_log(&args.log_file)?;
    let config = match &args.config {
        Some(p) => load_config(p)?,
        None => Config::default(),
    };

    let mut out = BufWriter::new(
        File::create(&args.out)
            .with_context(|| format!("creating output file {}", args.out.display()))?,
    );

    writeln!(out, "; Generated by oxide86-tools logs-to-asm")?;
    writeln!(
        out,
        "; Additional information can be found in tools/logs_to_asm.md"
    )?;
    writeln!(out, "; Log: {}", args.log_file.display())?;
    if let Some(cfg) = args.config {
        writeln!(out, "; Config: {}", cfg.display())?;
    }
    writeln!(out)?;

    generate(&mut out, &result, &config, args.hot_threshold)
}
